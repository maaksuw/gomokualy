
package apu;

/**
 * Tarjoaa laskemiseen liittyviä apumetodeja.
 */

public class Matikka {
    
    public static int max(int a, int b){
        if(a >= b) return a;
        else return b;
    }
    
    public static int min(int a, int b){
        if(a <= b) return a;
        else return b;
    }
    
}
